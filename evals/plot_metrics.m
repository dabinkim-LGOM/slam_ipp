function [] = plot_metrics(metrics)
% Plots logged informative metrics.

do_plot = 1;

text_size = 10.5;

time_vector = 0:0.1:200;

times = metrics.times;
P_traces = metrics.P_traces;
rmses = metrics.rmses;
mlls = metrics.mlls;

Rob_Ps_Aopt = [];
Rob_Ps_Dopt = [];

for i = 1:size(metrics.times,1)
    Rob_Ps_Aopt = [Rob_Ps_Aopt; trace(metrics.Rob_Ps(:,:,i))];
    Rob_Ps_Dopt = [Rob_Ps_Dopt; det(metrics.Rob_Ps(:,:,i))];
end

ts = timeseries(P_traces, times);
ts_resampled = resample(ts, time_vector, 'zoh');
P_traces_resampled = ts_resampled.data';

ts = timeseries(rmses, times);
ts_resampled = resample(ts, time_vector, 'zoh');
rmses_resampled = ts_resampled.data';

ts = timeseries(mlls, times);
ts_resampled = resample(ts, time_vector, 'zoh');
mlls_resampled = ts_resampled.data';

ts = timeseries(Rob_Ps_Aopt, times);
ts_resampled = resample(ts, time_vector, 'zoh');
Rob_Ps_Aopt_resampled = ts_resampled.data';

ts = timeseries(Rob_Ps_Dopt, times);
ts_resampled = resample(ts, time_vector, 'zoh');
Rob_Ps_Dopt_resampled = ts_resampled.data';

if (do_plot)

    % GP field covariance trace
    subplot(2,3,1)
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'off'      , ...
        'YTickMode'   , 'auto'  , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YScale'      , 'log'     , ...
        'YGrid'       , 'on'      , ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size , ...
        'LooseInset'  , max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, P_traces_resampled)
    axis([0 time_vector(end) 0 6*10^7])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('GP cov. trace');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    % RMSE
    subplot(2,3,2)
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , 0:1:8, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, rmses_resampled)
    axis([0 time_vector(end) 1 8.5])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('RMSE');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    % MLL
    subplot(2,3,3)
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , 0:2*10^7:10^8, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, mlls_resampled)
    axis([0 time_vector(end) 0 10^8])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('MLL');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    % Robot covariance trace (A-opt)
    subplot(2,3,4)
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , 0:0.05:1, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, Rob_Ps_Aopt_resampled)
    axis([0 time_vector(end) 0 0.2])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('Robot uncertainty @ A-opt');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    % Robot covariance determinant (D-opt)
    subplot(2,3,5)
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , 0:10^-4:10^-2, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, Rob_Ps_Dopt_resampled)
    axis([0 time_vector(end) 0 0.5*10^-4])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('Robot uncertainty @ D-opt');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    set(gcf, 'Position', [345, 146, 1208, 707]);
    
end

end